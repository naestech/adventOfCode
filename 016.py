from heapq import heappush, heappop
from collections import defaultdict

def parse_input(input_str):
    return [list(line.strip()) for line in input_str.strip().split('\n')]

def find_start_end(grid):
    start, end = None, None
    for y in range(len(grid)):
        for x in range(len(grid[0])):
            if grid[y][x] == 'S':
                start = (x, y)
            elif grid[y][x] == 'E':
                end = (x, y)
    return start, end

def get_neighbors(pos, direction, grid):
    x, y = pos
    dx, dy = direction
    
    # Possible moves: forward, turn left, turn right
    moves = [
        ((dx, dy), (x + dx, y + dy)),  # Forward
        ((-dy, dx), (x, y)),           # Turn left
        ((dy, -dx), (x, y))            # Turn right
    ]
    
    valid_moves = []
    for new_dir, (new_x, new_y) in moves:
        # Check if new position is within bounds and not a wall
        if (0 <= new_y < len(grid) and 
            0 <= new_x < len(grid[0]) and 
            grid[new_y][new_x] != '#'):
            # Cost is 1000 for turns, 1 for forward movement
            cost = 1000 if new_dir != direction else 1
            valid_moves.append((new_dir, (new_x, new_y), cost))
            
    return valid_moves

def find_shortest_path(grid):
    start, end = find_start_end(grid)
    # Start facing east (1, 0)
    initial_direction = (1, 0)
    
    # Priority queue for Dijkstra's algorithm
    queue = [(0, start, initial_direction)]
    # Keep track of visited states (position + direction)
    visited = defaultdict(lambda: float('inf'))
    visited[(start, initial_direction)] = 0
    
    while queue:
        cost, pos, direction = heappop(queue)
        
        if pos == end:
            return cost
            
        # Skip if we've found a better path to this state
        if cost > visited[(pos, direction)]:
            continue
            
        for new_dir, new_pos, move_cost in get_neighbors(pos, direction, grid):
            new_cost = cost + move_cost
            
            if new_cost < visited[(new_pos, new_dir)]:
                visited[(new_pos, new_dir)] = new_cost
                heappush(queue, (new_cost, new_pos, new_dir))
    
    return float('inf')

def find_shortest_path_and_tiles(grid):
    start, end = find_start_end(grid)
    initial_direction = (1, 0)
    
    queue = [(0, start, initial_direction, {start})]  # Added set of visited tiles
    visited = defaultdict(lambda: float('inf'))
    visited[(start, initial_direction)] = 0
    min_cost = float('inf')
    best_path_tiles = set()
    
    while queue:
        cost, pos, direction, path_tiles = heappop(queue)
        
        if cost > min_cost:  # Skip paths that are already worse than best
            continue
            
        if pos == end:
            if cost < min_cost:  # New best path
                min_cost = cost
                best_path_tiles = path_tiles.copy()
            elif cost == min_cost:  # Equally good path
                best_path_tiles.update(path_tiles)
            continue
            
        if cost > visited[(pos, direction)]:
            continue
            
        for new_dir, new_pos, move_cost in get_neighbors(pos, direction, grid):
            new_cost = cost + move_cost
            
            if new_cost <= visited[(new_pos, new_dir)]:
                visited[(new_pos, new_dir)] = new_cost
                new_path_tiles = path_tiles.copy()
                new_path_tiles.add(new_pos)
                heappush(queue, (new_cost, new_pos, new_dir, new_path_tiles))
    
    return min_cost, len(best_path_tiles)

